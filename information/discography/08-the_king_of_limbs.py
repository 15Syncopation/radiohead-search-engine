
overview = '''The King of Limbs is the eighth studio album by the English rock band Radiohead. It was self-released on 18 February 2011 as a download, followed by a physical release on 28 March through XL Recordings internationally and TBD Records in North America.

Following the protracted recording and more conventional instrumentation of In Rainbows (2007), Radiohead and their longtime producer Nigel Godrich developed The King of Limbs through sampling and looping. Singer Thom Yorke described the album as "an expression of wildness and mutation". The artwork, by Yorke and longtime collaborator Stanley Donwood, depicts nature and spirits inspired by fairy tales.

Radiohead released no singles from The King of Limbs, but released a music video for "Lotus Flower" featuring Yorke's dancing that spawned an internet meme. In 2012, they began an international tour, with several festival appearances. The European tour was postponed after the temporary stage collapsed in Toronto's Downsview Park, killing a technician and injuring three others.

The King of Limbs was named one of the best albums of the year by publications including The Wire, NME and PopMatters. It was nominated for five categories at the 54th Annual Grammy Awards, including the Grammy Award for Best Alternative Music Album. The download version sold an estimated 300,000 to 400,000 copies in two months, and the vinyl became a bestseller in the UK. The retail edition debuted at number seven on the UK Albums Chart and number six on the US Billboard 200. The King of Limbs was followed by the remix album TKOL RMX 1234567, the live video The King of Limbs: Live from the Basement, and the outtakes "Supercollider" and "The Butcher".'''


