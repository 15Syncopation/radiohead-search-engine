
overview = '''In Rainbows is the seventh studio album by the English rock band Radiohead. It was self-released on 10 October 2007 as a pay-what-you-want download, followed by a physical release internationally through XL Recordings in December 2007 and in North America through TBD Records on 1 January 2008. It was Radiohead's first release after their recording contract with EMI ended with their album Hail to the Thief (2003).

Radiohead began work on In Rainbows in early 2005. In 2006, after initial recording sessions with new producer Spike Stent proved fruitless, the band toured Europe and North America, performing the new material. After re-enlisting longtime producer Nigel Godrich, Radiohead recorded in the country houses Halswell House and Tottenham House, Godrich's London studio, and Radiohead's Oxfordshire studio. In Rainbows is more personal than previous Radiohead albums, with singer Thom Yorke describing it as "seduction songs".[1] Radiohead incorporated a variety of styles and instruments, using electronic instruments, strings, piano, and the ondes Martenot.

The pay-what-you-want release, the first for a major act, made headlines around the world and sparked debate about implications for the music industry. Radiohead promoted In Rainbows with webcasts, remix and music video competitions, a worldwide tour, and videos for the singles "Jigsaw Falling into Place", "Nude", "House of Cards" and "Reckoner".

The retail release topped the UK Albums Chart and the US Billboard 200, and by October 2008 In Rainbows had sold over three million copies worldwide. It received critical acclaim, winning Grammy Awards for Best Alternative Music Album and Best Boxed or Special Limited Edition Package, and was ranked one of the best albums of the year and the decade by various publications. Rolling Stone ranked In Rainbows on its updated lists of the 500 Greatest Albums of All Time at number 336 in 2012 and number 387 in 2020.'''


