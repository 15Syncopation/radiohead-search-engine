
overview = '''Hail to the Thief is the sixth album by English rock band Radiohead. It was released on 9 June 2003 through Parlophone internationally and a day later through Capitol Records in the United States. It was the last album released under Radiohead's record contract with EMI, parent company of Parlophone and Capitol.

After transitioning to a more electronic style on their albums Kid A (2000) and Amnesiac (2001), recorded through protracted studio experimentation, Radiohead sought to work more spontaneously, combining electronic and rock music. They recorded most of Hail to the Thief in two weeks in Los Angeles with longtime Radiohead producer Nigel Godrich, focusing on live takes rather than overdubs. Songwriter Thom Yorke wrote lyrics influenced by the unfolding war on terror and the "ignorance and stupidity" of the period. The cover artwork, created by longtime Radiohead artist Stanley Donwood, is a roadmap of Hollywood with words taken from roadside advertising in Los Angeles and from Yorke's lyrics.

Following a high-profile internet leak of unfinished material ten weeks before release, Hail to the Thief debuted at number one on the UK Albums Chart and number three on the US Billboard 200 chart, and was certified platinum in the UK, US and Canada. It produced three charting singles: "There There", "Go to Sleep" and "2 + 2 = 5". It received mostly positive reviews and was the fifth consecutive Radiohead album nominated for a Grammy Award for Best Alternative Music Album.'''


